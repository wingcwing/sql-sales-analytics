-- 2. Cumulative Analysis 
-- Aggregate the data progressively over time
-- [Cumulative Measure] By [Date Dimension]
-- e.g. Running Total Sales By Year
-- Moving Average of Sales By Month

-- Calculate the total sales per month
--and the running total of sales over time
SELECT 
	*,
	SUM (total_sales) OVER(PARTITION BY DATETRUNC(YEAR, order_date) ORDER BY order_date) AS running_total_sales
	FROM (
		SELECT 
			DATETRUNC(MONTH, order_date) AS order_date,
			SUM (sales_amount) AS total_sales
		FROM gold.fact_sales
		WHERE order_date IS NOT NULL
		GROUP BY DATETRUNC(MONTH, order_date)) t 


-- 3 months moving average of the sales
SELECT 
	*,
	avg (avg_sales_current_month) OVER(ORDER BY order_date ROWS BETWEEN 2 PRECEDING AND CURRENT ROW) AS '3_months_moving_sales'
	FROM (
		SELECT 
			DATETRUNC(MONTH, order_date) AS order_date,
			AVG (sales_amount) AS avg_sales_current_month
		FROM gold.fact_sales
