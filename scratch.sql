CREATE VIEW gold.report_products AS 
WITH basic_info AS (
SELECT 
	fs.order_number,
	fs.customer_key,
	dp.product_key,
	dp.product_id,
	dp.product_name,
	dp.category,
	dp.sub_category,
	dp.cost,
	fs.sales_amount, 
	fs.quantity,
	fs.order_date
FROM gold.fact_sales fs LEFT JOIN gold.dim_products dp
ON fs.product_key = dp.product_key 
WHERE order_date IS NOT NULL
)
, product_aggregation AS (SELECT 
	product_key,
	product_id,
	product_name,
	category,
	sub_category,
	cost,
	COUNT(DISTINCT(order_number)) AS total_orders,
	SUM(sales_amount) AS total_sales, 
	SUM(quantity) AS total_quantity,
	MAX(order_date) AS last_order_date,
	COUNT(DISTINCT(customer_key)) AS num_unique_cust,
	DATEDIFF(MONTH, MIN(order_date), MAX(order_date)) AS lifespan
FROM basic_info
GROUP BY product_key,
		product_id,
		product_name,
		category,
		sub_category,
		cost )
SELECT 	product_key,
	product_id,
	product_name,
	category,
	sub_category,
	cost,
	total_orders,
	total_sales,
	CASE WHEN total_sales > 50000 THEN 'High-Performers'
		 WHEN total_sales >= 10000 THEN 'Mid-Performers'
		 ELSE 'Low-Performers'
	END AS performance_segments,
	total_quantity,
	last_order_date,
	num_unique_cust,
	lifespan, 
	-- recency (months since last sale)
	DATEDIFF (MONTH, last_order_date, GETDATE()) AS recency,
    -- average order revenue (AOR)
	total_sales / NULLIF(total_orders,0) AS avg_order_revenue,
    -- average monthly revenue
	CASE WHEN lifespan = 0 THEN total_sales
		 ELSE total_sales / lifespan
	END AS avg_monthly_revenue
	FROM product_aggregation 
GO

