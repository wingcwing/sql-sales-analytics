-- 3. Performance Analysis 
-- Current[Measure] - Target[Sales] 

/* Analyze the yearly performance of products
by comparing each product's sales to both
its average sales performance and the previous year's sales.*/

WITH yearly_product_sales AS (
SELECT
	fc.product_key,
	YEAR(fc.order_date) AS order_year,
	dp.product_name,
	SUM(fc.sales_amount) AS current_sales
FROM gold.fact_sales fc LEFT JOIN gold.dim_products dp
ON fc.product_key =  dp.product_key
WHERE fc.order_date IS NOT NULL
GROUP BY fc.product_key,dp.product_name,YEAR (fc.order_date))
SELECT 
	*,
	AVG (current_sales) OVER (PARTITION BY product_name) AS avg_sales,
	current_sales - AVG (current_sales) OVER (PARTITION BY product_name) AS diff_avg,
	CASE WHEN current_sales - AVG (current_sales) OVER (PARTITION BY product_name)  > 0 THEN 'Above Average'
		 WHEN current_sales - AVG (current_sales) OVER (PARTITION BY product_name)  < 0 THEN 'Below Average'
			  ELSE 'Average'
		END avg_change, 
	-- Year-over-year Analysis
	LAG(current_sales, 1, NULL) OVER (PARTITION BY product_name ORDER BY order_year) AS previous_year_sales,
	current_sales - LAG(current_sales, 1, NULL) OVER (PARTITION BY product_name ORDER BY order_year) AS diff_yearly_sales, 
	CASE WHEN current_sales - LAG(current_sales, 1, NULL) OVER (PARTITION BY product_name ORDER BY order_year)  > 0 THEN 'Increase'
		 WHEN current_sales - LAG(current_sales, 1, NULL) OVER (PARTITION BY product_name ORDER BY order_year)  < 0 THEN 'Decrease'
			  ELSE 'Same'
		END previous_yaer_change
FROM yearly_product_sales
ORDER BY product_name, order_year

-- 4. Part-to-Whole Analysis (Proportional)
-- ([Measure]/Total[Measure]) * 100 By [Dimension]
-- E.g. (Sales / Total Sales) * 100 By Country

-- Which categories contribte the most to overall sales
WITH category_sales AS(
SELECT 
	dp.category,
	SUM (fs.sales_amount) AS cat_sales
FROM gold.fact_sales fs LEFT JOIN gold.dim_products dp
ON fs.product_key = dp.product_key
GROUP BY dp.category
)
SELECT 
	category, 
	cat_sales,
	SUM (cat_sales) OVER () AS total_sales,
	CAST(cat_sales * 100.0 / (SUM (cat_sales) OVER()) AS decimal(5,2)) AS percentage_of_total
FROM category_sales
ORDER BY percentage_of_total DESC

-- Data Segmentation (Group the data based on a specific range
-- [Measure] By [Measure]
-- e.g. Total Products By Sales Range, Total Customers By Age Range

/*Segment products into cost ranges and
count how many products fall into each segment*/
WITH product_segments AS(
SELECT 
	product_key,
	product_name,
	cost, 
	CASE WHEN cost < 100 THEN 'Below 100'
		WHEN cost BETWEEN 100 AND 500 THEN '100-500'
		WHEN cost BETWEEN 500 AND 1000 THEN '500-1000'
		ELSE 'Above 1000'
	END cost_range
FROM gold.dim_products
)
SELECT 
	cost_range,
	COUNT(product_key) AS total_products
FROM product_segments
GROUP BY cost_range
ORDER BY total_products DESC

/*Group customers into three segments based on their spending behavior:
- VIP: Customers with at least 12 months of history and spending more than €5,000.
- Regular: Customers with at least 12 months of history but spending €5,000 or less
- New: Customers with a lifespan less than 12 months.
And find the total number of customers by each group*/

WITH customer_spending AS (
    SELECT
        customer_key, 
        SUM(sales_amount) AS total_spending,
        DATEDIFF(MONTH, MIN(order_date), MAX(order_date)) AS lifespan
    FROM gold.fact_sales
    GROUP BY customer_key
),
customer_segments AS (
    SELECT
        customer_key,
        CASE 
            WHEN lifespan < 12 THEN 'New'
            WHEN total_spending > 5000 THEN 'VIP'
            ELSE 'Regular'
        END AS customer_category
    FROM customer_spending
)
SELECT
    customer_category,
    COUNT(customer_key) AS number_customer
FROM customer_segments
GROUP BY customer_category
ORDER BY number_customer DESC;

