CREATE VIEW gold.report_customers AS 
WITH basic_info AS (
/*---------------------------------------------------------------------------
1) Base Query: Retrieves core columns from tables
---------------------------------------------------------------------------*/
SELECT 
	fs.order_number,
	fs.product_key,
	fs.order_date,
	fs.sales_amount,
	fs.quantity,
	dc.customer_key,
	CONCAT (dc.first_name, ' ', dc.last_name) AS name,
	dc.country,
	DATEDIFF(YEAR, dc.birthday, GETDATE()) AS age,
	dc.gender
FROM gold.fact_sales fs LEFT JOIN gold.dim_customers dc
ON fs.customer_key = dc.customer_key
WHERE order_date IS NOT NULL
) , customer_aggregation AS (
/*---------------------------------------------------------------------------
2) Customer Aggregations: Summarizes key metrics at the customer level
---------------------------------------------------------------------------*/
SELECT 
	customer_key,
	name,
	age,
	gender,
	SUM(sales_amount) AS total_sales,
	COUNT(DISTINCT order_number) AS total_orders,
	SUM(quantity) AS total_quantity,
	COUNT(DISTINCT product_key) AS total_products,
	MAX(order_date) AS last_order_date,
	DATEDIFF(MONTH,MIN (order_date),MAX(order_date)) AS lifespan
FROM basic_info
GROUP BY 
	customer_key,
	name,
	age,
	gender)
SELECT 
	customer_key,
	name, 
	gender,
	age, 
	CASE 
	 WHEN age >= 50 THEN '50 and Above'
	 WHEN age >= 40 THEN '40-49'
	 WHEN age >= 30 THEN '30-39'
	 WHEN age >= 20 THEN '20-29'
	 ELSE 'Below 20'
	END AS age_group,
	CASE WHEN lifespan < 12 THEN 'New'
         WHEN total_sales > 5000 THEN 'VIP'
         ELSE 'Regular'
	END AS customer_segments,
	total_sales,
	total_orders,
	total_quantity,
	total_products,
	lifespan,
	DATEDIFF(MONTH, last_order_date, GETDATE()) AS recency,
	--average order value
	total_sales / total_orders AS avg_order_value,
	--average monthly spend
	CASE WHEN lifespan = 0 THEN total_sales 
		 ELSE total_sales / lifespan 
	END AS avg_monthly_spend
FROM customer_aggregation

